    def _collect_bookmarks_epsg3857(self):
        """プロジェクト/ユーザブックマークを収集し中心座標をEPSG:3857に変換して返す。

        Returns: [{ 'name': str, 'x': float, 'y': float }]
        """
        out = []
        try:
            from qgis.core import QgsProject, QgsCoordinateReferenceSystem, QgsCoordinateTransform, QgsPointXY, QgsApplication
        except Exception:
            return out
        try:
            project = QgsProject.instance()
            bookmark_mgr = project.bookmarkManager() if project else None
            proj_bms = bookmark_mgr.bookmarks() if bookmark_mgr else []
        except Exception:
            proj_bms = []
        try:
            app_bm_mgr = QgsApplication.bookmarkManager()
            app_bms = app_bm_mgr.bookmarks() if app_bm_mgr else []
        except Exception:
            app_bms = []
        all_bms = list(proj_bms) + list(app_bms)
        try:
            dest = QgsCoordinateReferenceSystem('EPSG:3857')
        except Exception:
            return out
        for bm in all_bms:
            try:
                name = bm.name() if hasattr(bm, 'name') else ''
                extent = bm.extent() if hasattr(bm, 'extent') else None
                if not extent:
                    continue
                cx = extent.center().x()
                cy = extent.center().y()
                src = extent.crs()
                if src is None or not src.isValid():
                    # 推定: 値が大きければ既に3857、それ以外は4326
                    src = QgsCoordinateReferenceSystem('EPSG:3857' if (abs(cx) > 180 or abs(cy) > 180) else 'EPSG:4326')
                if src.authid() == 'EPSG:3857':
                    tx, ty = cx, cy
                else:
                    t = QgsCoordinateTransform(src, dest, project)
                    p = t.transform(QgsPointXY(cx, cy))
                    tx, ty = p.x(), p.y()
                out.append({'name': name, 'x': float(tx), 'y': float(ty)})
            except Exception:
                continue
        return out

    def _serve_static_and_get_url(self, folder_path: str, preferred_port: int = 8091) -> str:
        """指定フォルダを簡易HTTPサーバで配信し、index.html のURLを返す。

        - Python 標準の SimpleHTTPRequestHandler を使用（別スレッドで起動）
        - 既にサーバが起動済みの場合は再利用し、最新のフォルダを配信する
        """
        try:
            import threading
            import socket
            from http.server import SimpleHTTPRequestHandler
            from socketserver import TCPServer
            import functools

            # 既存サーバの停止（フォルダを切り替えるため）
            try:
                if hasattr(self, '_static_server') and self._static_server:
                    self._static_server.shutdown()
                    self._static_server.server_close()
                    self._static_server = None
            except Exception:
                pass

            # 使用可能なポートを検索
            port = None
            for p in range(preferred_port, preferred_port + 20):
                with socket.socket(socket.AF_INET, socket.SOCK_STREAM) as s:
                    s.settimeout(0.2)
                    try:
                        s.bind(('127.0.0.1', p))
                        port = p
                        break
                    except Exception:
                        continue
            if port is None:
                port = preferred_port

            # ディレクトリを指定してハンドラ生成（chdir不要）
            # ヘッダに charset=UTF-8 を付与して文字化けを防止
            class UTF8Handler(SimpleHTTPRequestHandler):
                extensions_map = SimpleHTTPRequestHandler.extensions_map.copy()
                extensions_map.update({
                    '.html': 'text/html; charset=UTF-8',
                    '.htm': 'text/html; charset=UTF-8',
                    '.css': 'text/css; charset=UTF-8',
                    '.js': 'application/javascript; charset=UTF-8',
                })

            handler = functools.partial(UTF8Handler, directory=folder_path)

            class ReuseTCPServer(TCPServer):
                allow_reuse_address = True

            server = ReuseTCPServer(('127.0.0.1', port), handler)
            self._static_server = server

            t = threading.Thread(target=server.serve_forever, name='StaticHTTP', daemon=True)
            t.start()
            self._static_server_thread = t

            return f"http://127.0.0.1:{port}/index.html"
        except Exception as e:
            self._qgis_log(f"[OpenLayers] Static HTTP server failed: {e}", 'WARNING')
            raise

